if( ! function_exists('year_list') )
{
    function year_list()
    {
        $options = array('' => 'Select Year');
        for($i=0; $i<=8; $i++)
        {
            $year = date('Y');
            $yr = date('y');
            $options[$yr+$i] = $year+$i;
        }
        return $options;
    }
